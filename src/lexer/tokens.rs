#[derive(Debug, Clone, PartialEq)]
pub enum Token {
    VOID,
    INT,
    SEMIC,
    LCURL,
    RCURL,
    LPAREN,
    RPAREN,
    RETURN,
    INTLITERAL,
    ADD,
    SUB,
    MUL,
    DIV,
    REF,
    AND,
    OR,
    EQ,
    NE,
    LE,
    GE,
    LT,
    GT,
    ASSIGN,
    LOGNEG,
    IF,
    WHILE,
    FOR,
    ELSE,
    BREAK,
    CONTINUE,
    COMMA,
    MOD,
    CHAR,
    CHARLITERAL,
    LONG,
    LBRACE,
    RBRACE,
    IDENT,
    EOF,
    ERR,
}

impl Token {
    pub fn from(value: usize) -> Token {
        match value {
            0 => Token::VOID,
            1 => Token::INT,
            2 => Token::SEMIC,
            3 => Token::LCURL,
            4 => Token::RCURL,
            5 => Token::LPAREN,
            6 => Token::RPAREN,
            7 => Token::RETURN,
            8 => Token::INTLITERAL,
            9 => Token::ADD,
            10 => Token::SUB, 
            11 => Token::MUL,
            12 => Token::DIV,
            13 => Token::REF,
            14 => Token::AND,
            15 => Token::OR,
            16 => Token::EQ,
            17 => Token::NE,
            18 => Token::LE,
            19 => Token::GE,
            20 => Token::LT,
            21 => Token::GT,
            22 => Token::ASSIGN,
            23 => Token::LOGNEG,
            24 => Token::IF,
            25 => Token::WHILE, 
            26 => Token::FOR,
            27 => Token::ELSE,
            28 => Token::BREAK,
            29 => Token::CONTINUE,
            30 => Token::COMMA,
            31 => Token::MOD,
            32 => Token::CHAR,
            33 => Token::CHARLITERAL,
            34 => Token::LONG,
            35 => Token::LBRACE,
            36 => Token::RBRACE,        
            37 => Token::IDENT,
            38 => Token::EOF,
            _ => Token::ERR
        }
    }
}

